function coordAtProgress(coords, progress) {
  const target = Math.max(0, Math.min(1, progress));
  const segmentLengths = [];
  let total = 0;
  for (let i = 1; i < coords.length; i += 1) {
    const length = distanceMeters(coords[i - 1], coords[i]);
    segmentLengths.push(length);
    total += length;
  }
  if (!total) return { coord: coords[0], bearing: 0 };

  let remaining = target * total;
  for (let i = 1; i < coords.length; i += 1) {
    const length = segmentLengths[i - 1];
    if (remaining <= length || i === coords.length - 1) {
      const ratio = length ? remaining / length : 0;
      const a = coords[i - 1];
      const b = coords[i];
      return {
        coord: [
          a[0] + (b[0] - a[0]) * ratio,
          a[1] + (b[1] - a[1]) * ratio,
        ],
        bearing: bearingDegrees(a, b),
      };
    }
    remaining -= length;
  }
  return { coord: coords[coords.length - 1], bearing: bearingDegrees(coords[coords.length - 2], coords[coords.length - 1]) };
}

function routeStopProgress(route, stopIndex) {
  const distances = routeStopDistances(route);
  const distance = distances[stopIndex] || 0;
  const profile = routeDistanceProfile(route);
  if (!profile.total) return 0;
  return Math.max(0, Math.min(1, distance / profile.total));
}

function routeStopOffsetsMinutes(route) {
  const offsets = route.schedule && route.schedule.stopOffsetsMinutes;
  if (!Array.isArray(offsets)) return null;
  return offsets.map((value) => Number(value));
}

function routeStopOffsetMinutes(route, stopIndex, tripMinutes = route.schedule && route.schedule.estimatedTripMinutes || 15) {
  const segmentOffset = segmentBasedRouteStopOffsetMinutes(route, stopIndex, tripMinutes);
  if (Number.isFinite(segmentOffset)) return Math.max(0, Math.min(tripMinutes + 8, segmentOffset));
  return baseRouteStopOffsetMinutes(route, stopIndex, tripMinutes);
}

function baseRouteStopOffsetMinutes(route, stopIndex, tripMinutes = route.schedule && route.schedule.estimatedTripMinutes || 15) {
  const offsets = routeStopOffsetsMinutes(route);
  const explicit = offsets && offsets[stopIndex];
  if (Number.isFinite(explicit)) return Math.max(0, Math.min(tripMinutes, explicit));
  return tripMinutes * routeStopProgress(route, stopIndex);
}

function segmentBasedRouteStopOffsetMinutes(route, stopIndex, tripMinutes = route.schedule && route.schedule.estimatedTripMinutes || 15) {
  if (!route || stopIndex < 0 || !Array.isArray(route.stops)) return NaN;
  let offset = baseRouteStopOffsetMinutes(route, 0, tripMinutes);
  for (let i = 1; i <= stopIndex; i += 1) {
    offset += routeSegmentTravelMinutes(route, i, tripMinutes);
  }
  return offset;
}

function routeSegmentTravelMinutes(route, toStopIndex, tripMinutes = route.schedule && route.schedule.estimatedTripMinutes || 15) {
  if (!route || !Array.isArray(route.stops) || toStopIndex <= 0 || toStopIndex >= route.stops.length) return 0;
  const fromStopId = route.stops[toStopIndex - 1];
  const toStopId = route.stops[toStopIndex];
  const calibrated = defaultSegmentTravelMinutes(route.id, fromStopId, toStopId);
  if (Number.isFinite(calibrated)) return Math.max(0, calibrated);
  const basePrevious = baseRouteStopOffsetMinutes(route, toStopIndex - 1, tripMinutes);
  const baseCurrent = baseRouteStopOffsetMinutes(route, toStopIndex, tripMinutes);
  return Math.max(0, baseCurrent - basePrevious);
}

function defaultSegmentTravelMinutes(routeId, fromStopId, toStopId) {
  const key = segmentKey(fromStopId, toStopId);
  const routeKey = `${routeId}:${key}`;
  const item = DEFAULT_ROUTE_SEGMENT_TRAVEL_MINUTES[routeKey] || DEFAULT_SEGMENT_TRAVEL_MINUTES[key];
  return item && Number.isFinite(item.minutes) ? item.minutes : NaN;
}

function segmentKey(fromStopId, toStopId) {
  return `${fromStopId}->${toStopId}`;
}

function effectiveRouteTripMinutes(route, scheduledTripMinutes = route && route.schedule && route.schedule.estimatedTripMinutes || 15) {
  if (!route || !Array.isArray(route.stops) || !route.stops.length) return scheduledTripMinutes;
  const finalOffset = segmentBasedRouteStopOffsetMinutes(route, route.stops.length - 1, scheduledTripMinutes);
  return Number.isFinite(finalOffset) && finalOffset > 0
    ? Math.max(1, finalOffset)
    : scheduledTripMinutes;
}

function routeProgressAtElapsed(route, elapsedMinutes, tripMinutes = route.schedule && route.schedule.estimatedTripMinutes || 15, at = new Date()) {
  const fallback = Math.max(0, Math.min(1, elapsedMinutes / tripMinutes));

  const checkpoints = (route.stops || [])
    .map((_, stopIndex) => ({
      stopIndex,
      minutes: routeStopOffsetMinutes(route, stopIndex, tripMinutes),
      progress: routeStopProgress(route, stopIndex),
    }))
    .filter((item) => Number.isFinite(item.minutes) && Number.isFinite(item.progress))
    .sort((a, b) => a.minutes - b.minutes);

  if (checkpoints.length < 2) return fallback;
  const routeEnd = Math.max(tripMinutes, checkpoints[checkpoints.length - 1].minutes);
  const elapsed = Math.max(0, Math.min(routeEnd, elapsedMinutes));
  if (elapsed <= checkpoints[0].minutes) return checkpoints[0].progress;

  for (let i = 1; i < checkpoints.length; i += 1) {
    const previous = checkpoints[i - 1];
    const next = checkpoints[i];
    if (elapsed > next.minutes && i < checkpoints.length - 1) continue;
    const span = next.minutes - previous.minutes;
    if (span <= 0) return next.progress;
    const dwellMinutes = routeStopDwellMinutes(route, previous.stopIndex, span, at);
    const departTime = previous.minutes + dwellMinutes;
    if (elapsed <= departTime) return previous.progress;
    const motionSpan = Math.max(0.05, next.minutes - departTime);
    const ratio = (elapsed - departTime) / motionSpan;
    return Math.max(0, Math.min(1, previous.progress + (next.progress - previous.progress) * ratio));
  }

  return checkpoints[checkpoints.length - 1].progress;
}

function routeStopDwellMinutes(route, stopIndex, segmentMinutes = 1, at = new Date()) {
  if (!route || !Array.isArray(route.stops) || stopIndex < 0 || stopIndex >= route.stops.length - 1) return 0;
  const stopId = route.stops[stopIndex];
  const seconds = defaultStopDwellSeconds(stopId, at);
  return Math.max(
    0,
    Math.min(STOP_DWELL_MAX_SECONDS, seconds, segmentMinutes * 60 * STOP_DWELL_SEGMENT_MAX_RATIO)
  ) / 60;
}

function defaultStopDwellSeconds(stopId, at = new Date()) {
  const base = DEFAULT_STOP_DWELL_SECONDS[stopId] || DEFAULT_STOP_DWELL_SECONDS_FALLBACK;
  return Math.round(base * stopDwellTimeMultiplier(at));
}

function stopDwellTimeMultiplier(at = new Date()) {
  const clock = hongKongClock(at);
  const minute = clock.minutes;
  let multiplier = 1;
  if (minute < 330) multiplier = 0.75;
  else if (minute < 450) multiplier = 0.85;
  else if (minute < 570) multiplier = 1.45;
  else if (minute >= 690 && minute < 840) multiplier = 1.2;
  else if (minute >= 990 && minute < 1170) multiplier = 1.35;
  else if (minute >= 1170) multiplier = 0.85;
  const dayMultiplier = clock.weekday >= 1 && clock.weekday <= 5 ? 1 : 0.8;
  return multiplier * dayMultiplier;
}

function routeStopDistances(route) {
  const cacheKey = route.id;
  if (ROUTE_STOP_DISTANCE_CACHE.has(cacheKey)) return ROUTE_STOP_DISTANCE_CACHE.get(cacheKey);

  const profile = routeDistanceProfile(route);
  let previousDistance = 0;
  const distances = (route.stops || []).map((stopId, stopIndex) => {
    const stop = root.CUHK_BUS_DATA.stops[stopId];
    if (!stop || !stop.coord || !profile.total) return previousDistance;
    const minDistance = stopIndex === 0 ? 0 : Math.max(0, previousDistance - 35);
    const projection = projectCoordToRoute(stop.coord, profile, minDistance) || projectCoordToRoute(stop.coord, profile, 0);
    if (!projection) return previousDistance;
    previousDistance = Math.max(previousDistance, projection.distance);
    return previousDistance;
  });
  ROUTE_STOP_DISTANCE_CACHE.set(cacheKey, distances);
  return distances;
}

function routeDistanceProfile(route) {
  const coords = routeCoordinates(route);
  const segmentLengths = [];
  const cumulative = [0];
  let total = 0;
  for (let i = 1; i < coords.length; i += 1) {
    const length = distanceMeters(coords[i - 1], coords[i]);
    segmentLengths.push(length);
    total += length;
    cumulative.push(total);
  }
  return { coords, segmentLengths, cumulative, total };
}

function projectCoordToRoute(coord, profile, minDistance = 0) {
  let best = null;
  const comparableErrorMeters = 8;
  for (let i = 1; i < profile.coords.length; i += 1) {
    const segmentStartDistance = profile.cumulative[i - 1];
    const segmentEndDistance = profile.cumulative[i];
    if (segmentEndDistance < minDistance) continue;

    const projected = projectCoordToSegment(coord, profile.coords[i - 1], profile.coords[i]);
    const distance = segmentStartDistance + profile.segmentLengths[i - 1] * projected.ratio;
    if (distance + 1 < minDistance) continue;
    if (
      !best ||
      projected.errorMeters < best.errorMeters - comparableErrorMeters ||
      (Math.abs(projected.errorMeters - best.errorMeters) <= comparableErrorMeters && distance < best.distance)
    ) {
      best = { distance, errorMeters: projected.errorMeters };
    }
  }
  return best;
}

function projectCoordToSegment(point, a, b) {
  const latScale = 111320;
  const lonScale = Math.cos(toRad((point[1] + a[1] + b[1]) / 3)) * 111320;
  const bx = (b[0] - a[0]) * lonScale;
  const by = (b[1] - a[1]) * latScale;
  const px = (point[0] - a[0]) * lonScale;
  const py = (point[1] - a[1]) * latScale;
  const lengthSq = bx * bx + by * by;
  const ratio = lengthSq ? Math.max(0, Math.min(1, (px * bx + py * by) / lengthSq)) : 0;
  const dx = px - bx * ratio;
  const dy = py - by * ratio;
  return { ratio, errorMeters: Math.hypot(dx, dy) };
}

function distanceMeters(a, b) {
  const radius = 6371000;
  const lat1 = toRad(a[1]);
  const lat2 = toRad(b[1]);
  const dLat = toRad(b[1] - a[1]);
  const dLon = toRad(b[0] - a[0]);
  const h = Math.sin(dLat / 2) ** 2 + Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLon / 2) ** 2;
  return 2 * radius * Math.atan2(Math.sqrt(h), Math.sqrt(1 - h));
}

function bearingDegrees(a, b) {
  const lat1 = toRad(a[1]);
  const lat2 = toRad(b[1]);
  const dLon = toRad(b[0] - a[0]);
  const y = Math.sin(dLon) * Math.cos(lat2);
  const x = Math.cos(lat1) * Math.sin(lat2) - Math.sin(lat1) * Math.cos(lat2) * Math.cos(dLon);
  return (Math.atan2(y, x) * 180 / Math.PI + 360) % 360;
}

function toRad(value) {
  return value * Math.PI / 180;
}

