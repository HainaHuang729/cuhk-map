function departuresForRule(rule) {
  const first = parseTime(rule.first);
  const last = parseTime(rule.last);
  const startHour = Math.floor(first / 60) * 60;
  const departures = [];
  for (let hour = startHour; hour <= last; hour += 60) {
    rule.departMinutes.forEach((minute) => {
      const time = hour + minute;
      if (time >= first && time <= last) departures.push(time);
    });
  }
  return [...new Set(departures)].sort((a, b) => a - b);
}

function todayRules(route, clock = hongKongClock()) {
  if (!route.schedule) return [];
  return route.schedule.rules.filter((rule) => rule.days.includes(clock.weekday));
}

function activeVehicles(route, at = new Date()) {
  const schedule = route.schedule;
  const coords = routeCoordinates(route);
  if (!schedule || coords.length < 2) return [];

  const clock = hongKongClock(at);
  const tripMinutes = effectiveRouteTripMinutes(route);
  const vehicles = [];
  todayRules(route, clock).forEach((rule) => {
    departuresForRule(rule).forEach((departure) => {
      const elapsed = clock.minutes - departure;
      if (elapsed < 0 || elapsed > tripMinutes) return;
      const progress = routeProgressAtElapsed(route, elapsed, tripMinutes, at);
      const point = coordAtProgress(coords, progress);
      vehicles.push({
        id: `${route.id}-${formatTime(departure)}`,
        route,
        departure,
        elapsedMinutes: elapsed,
        tripMinutes,
        progress,
        coord: point.coord,
        bearing: point.bearing,
        ruleLabel: rule.label,
      });
    });
  });
  return vehicles.sort((a, b) => a.departure - b.departure);
}

function nextDeparture(route, at = new Date()) {
  if (!route.schedule) return null;
  const clock = hongKongClock(at);
  let next = null;
  todayRules(route, clock).forEach((rule) => {
    departuresForRule(rule).forEach((departure) => {
      if (departure < clock.minutes) return;
      if (!next || departure < next.departure) {
        next = { departure, ruleLabel: rule.label };
      }
    });
  });
  return next;
}

function nextStopArrival(route, stopIndex, at = new Date()) {
  const schedule = route.schedule;
  if (!schedule) {
    return { route, stopIndex, status: "no_schedule", label: "暂无官方时刻表" };
  }

  const clock = hongKongClock(at);
  const rules = todayRules(route, clock);
  if (!rules.length) {
    return { route, stopIndex, status: "no_service_today", label: "今日无常规班次", clock };
  }

  const tripMinutes = effectiveRouteTripMinutes(route);
  const stopProgress = routeStopProgress(route, stopIndex);
  const offsetMinutes = routeStopOffsetMinutes(route, stopIndex, tripMinutes);
  let next = null;

  rules.forEach((rule) => {
    departuresForRule(rule).forEach((departure) => {
      const arrival = departure + offsetMinutes;
      const graceMinutes = stopIndex === 0 ? 0.75 : 0.25;
      if (arrival + graceMinutes < clock.minutes) return;
      if (!next || arrival < next.arrival) {
        next = {
          route,
          stopIndex,
          status: "scheduled",
          departure,
          arrival,
          etaMinutes: Math.max(0, arrival - clock.minutes),
          offsetMinutes,
          progress: stopProgress,
          ruleLabel: rule.label,
          clock,
        };
      }
    });
  });

  if (!next) {
    return { route, stopIndex, status: "ended", label: "今日班次已结束", clock };
  }
  next.departureLabel = formatTime(next.departure);
  next.arrivalLabel = formatTime(next.arrival);
  next.label = stopArrivalSummary(next);
  return next;
}

function nextStopArrivals(items, at = new Date()) {
  const byRoute = new Map();
  items.forEach((item) => {
    if (!item || !item.route || !Number.isInteger(item.stopIndex)) return;
    const arrival = nextStopArrival(item.route, item.stopIndex, at);
    const enriched = {
      ...arrival,
      direction: stopItemDirection(item),
      item,
    };
    enriched.label = stopArrivalSummary(enriched);
    const current = byRoute.get(item.route.id);
    if (!current || compareStopArrival(enriched, current) < 0) {
      byRoute.set(item.route.id, enriched);
    }
  });
  return [...byRoute.values()].sort(compareStopArrival);
}

function compareStopArrival(a, b) {
  const aScheduled = a && a.status === "scheduled";
  const bScheduled = b && b.status === "scheduled";
  if (aScheduled && bScheduled) return a.arrival - b.arrival;
  if (aScheduled) return -1;
  if (bScheduled) return 1;
  return String(a && a.route && a.route.id || "").localeCompare(String(b && b.route && b.route.id || ""), "zh-Hant");
}

function stopArrivalSummary(arrival) {
  if (!arrival) return "暂无预计到站";
  if (arrival.status === "scheduled") {
    const directionText = arrival.direction ? ` · ${arrival.direction}` : "";
    return `预计下一班 ${formatTime(arrival.arrival)}（${arrivalEtaText(arrival)}${directionText}）`;
  }
  if (arrival.status === "no_service_today") return "今日无常规班次";
  if (arrival.status === "ended") return "今日班次已结束";
  if (arrival.status === "no_schedule") return "暂无官方时刻表";
  return arrival.label || "暂无预计到站";
}

function arrivalEtaText(arrival) {
  if (!arrival || arrival.status !== "scheduled") return "暂无 ETA";
  if (arrival.stopIndex === 0 && Number.isFinite(arrival.etaMinutes) && arrival.etaMinutes < 0.75) return "即将出发";
  return formatEtaMinutes(arrival.etaMinutes);
}

function formatEtaMinutes(value) {
  if (!Number.isFinite(value)) return "暂无 ETA";
  if (value < 0.75) return "即将到达";
  return `约 ${Math.ceil(value)} 分钟`;
}

function serviceStatus(route, at = new Date()) {
  const schedule = route.schedule;
  if (!schedule) {
    return { label: "暂无官方时刻表", activeCount: 0, hasServiceToday: false, next: null };
  }
  const clock = hongKongClock(at);
  const rules = todayRules(route, clock);
  const vehicles = activeVehicles(route, at);
  const next = nextDeparture(route, at);
  const activeText = vehicles.length ? `运行中 ${vehicles.length} 辆` : "暂无运行车辆";
  const nextText = next
    ? `下一班 ${formatTime(next.departure)}`
    : (rules.length ? "今日班次已结束" : "今日无常规班次");
  return {
    label: `${activeText}；${nextText}`,
    activeCount: vehicles.length,
    hasServiceToday: rules.length > 0,
    next,
    clock,
  };
}

